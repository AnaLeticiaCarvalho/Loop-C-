// Do While

int soma = 0, numero = 0;
do
{
    Console.WriteLine("Digite um número: (0 para parar) ");
    numero = Convert.ToInt32(Console.ReadLine());
    soma += numero;

} while (numero != 0);

Console.WriteLine("A soma de todos os números digitados é: {soma}");


